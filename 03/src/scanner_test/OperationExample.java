package scanner_test;
/* * 클래스 종류 3가지
 * 1. main()있는 것 : 실행용
 * 2. main() 없는 것: 라이브러리용 => 5장
 * 3. 실행용 + 라이브러리용 
 * 
 */
public class OperationExample {

	public static void main(String[] args) {
		/*-- 산술 연산자 + - * 나누기(몫/ 나머지 %) */
		int i1 = 10;
		int i2 = 3;
		
		System.out.println(i1 + i2);
		System.out.println(i1 - i2);
		System.out.println(i1 * i2);
		System.out.println(i1 / i2);
		System.out.println(i1 % i2);
		
		
	}

}
